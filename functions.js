export function add(addArgument1, addArgument2) {
    return Number(addArgument1) + Number(addArgument2);
}

export function subtract(subArgument1, subArgument2) {
    return Number(subArgument1) - Number(subArgument2);
}

export function multiply(multArgument1, multArgument2) {
    return Number(multArgument1) * Number(multArgument2);
}

export function divide(divArgument1, divArgument2) {
    return Number(divArgument1) / Number(divArgument2);
}

